function count(){
	var 起點站名=document.StartStation.Location.value;
	var 終點站名=document.EndStation.Location.value;
	var 區間車=document.getElementById('區間車');
	var 莒光號=document.getElementById('莒光號');
	var 自強號=document.getElementById('自強號');
	var 區間電子=document.getElementById('區間電子');
	var 自強電子=document.getElementById('自強電子');
	var 更多=document.getElementById("viewmore");
	var 時刻表=document.getElementById("tratime");
	var 起點_環狀=0;var 起點_基隆=0;var 起點_平溪=0;var 起點_深澳=0;var 起點_內灣=0;var 起點_六家=0;var 起點_集集=0;var 起點_沙崙=0;
	var 終點_環狀=0;var 終點_基隆=0;var 終點_平溪=0;var 終點_深澳=0;var 終點_內灣=0;var 終點_六家=0;var 終點_集集=0;var 終點_沙崙=0;
	var 距離=0;
	時刻表.style.display=更多.style.display;
	

	var idx_start=0;var idx_end=0;
	for(idx_start=0;;idx_start++){
		if(Data[idx_start].name===起點站名)break;
	}
	for(idx_end=0;;idx_end++){
		if(Data[idx_end].name===終點站名)break;
	}
	if(Data[idx_start].line==Data[idx_end].line){
		距離=Math.abs(Data[idx_end].km-Data[idx_start].km);
		if(Data[idx_start].line=="環狀線"&&Data[idx_end].line=="環狀線"){
			if(875.9-距離<距離)距離=875.9-距離;
		}
	}
	else{
		if(Data[idx_start].line=="環狀線")起點_環狀=Data[idx_start].km;
		else if(Data[idx_start].line=="基隆線"){起點_基隆=Data[idx_start].km;起點_環狀=0}
		else if(Data[idx_start].line=="平溪線"){起點_平溪=Data[idx_start].km;起點_環狀=-16.0}
		else if(Data[idx_start].line=="深澳線"){起點_深澳=Data[idx_start].km;起點_環狀=-8.9}
		else if(Data[idx_start].line=="內灣線"){起點_內灣=Data[idx_start].km;起點_環狀=101.3}
		else if(Data[idx_start].line=="集集線"){起點_集集=Data[idx_start].km;起點_環狀=239.2}
		else if(Data[idx_start].line=="沙崙線"){起點_沙崙=Data[idx_start].km;起點_環狀=361.1}
		
		if(Data[idx_end].line=="環狀線")終點_環狀=Data[idx_end].km;
		else if(Data[idx_end].line=="基隆線"){終點_基隆=Data[idx_end].km;終點_環狀=0}
		else if(Data[idx_end].line=="平溪線"){終點_平溪=Data[idx_end].km;終點_環狀=-16.0}
		else if(Data[idx_end].line=="深澳線"){終點_深澳=Data[idx_end].km;終點_環狀=-8.9}
		else if(Data[idx_end].line=="內灣線"){終點_內灣=Data[idx_end].km;終點_環狀=101.3}
		else if(Data[idx_end].line=="集集線"){終點_集集=Data[idx_end].km;終點_環狀=239.2}
		else if(Data[idx_end].line=="沙崙線"){終點_沙崙=Data[idx_end].km;終點_環狀=361.1}
		
		距離=Math.abs(起點_環狀-終點_環狀);
		if(875.9-距離<距離)距離=875.9-距離;
		距離=距離+起點_基隆+起點_平溪+起點_深澳+起點_內灣+起點_集集+起點_沙崙;
		距離=距離+終點_基隆+終點_平溪+終點_深澳+終點_內灣+終點_集集+終點_沙崙;
	}

	var road=距離;
	if(road<10)road=10;

	區間車.innerHTML=Math.round(road*1.46);
	莒光號.innerHTML=Math.round(road*1.75);
	自強號.innerHTML=Math.round(road*2.27);
	區間電子.innerHTML=Math.floor(Math.round(road*1.46)*0.9);
	自強電子.innerHTML=Math.floor(Math.round((road<=70?road:70)*1.46)*0.9+(road>70?road-70:0)*2.27);
}

